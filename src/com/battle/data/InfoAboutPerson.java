package com.battle.data;

public class InfoAboutPerson {
    private String characterPerson;
    private String namePerson;
    private String nameSquad;

    public String getCharacterPerson() {
        return characterPerson;
    }

    public void setCharacterPerson(String characterPerson) {
        this.characterPerson = characterPerson;
    }

    public String getNamePerson() {
        return namePerson;
    }

    public void setNamePerson(String namePerson) {
        this.namePerson = namePerson;
    }

    public String getNameSquad() {
        return nameSquad;
    }

    public void setNameSquad(String nameSquad) {
        this.nameSquad = nameSquad;
    }
}
